#include "../minishell.h"

t_list	*ft_lstnew(char *content)
{
	t_list	*new_lst;

	new_lst = (t_list *)malloc(sizeof(t_list));
	if (!new_lst)
		return (NULL);
	new_lst->content = content;
	new_lst->type = NONE;
	new_lst->next = NULL;
	return (new_lst);
}
